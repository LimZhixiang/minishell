#include "libft.h"

long	ft_atol(const char *str)
{
	long	sign;
	long	i;

	i = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
		{
			sign = -1;
		}
		str++;
	}
	while ((*str >= '0' && *str <= '9') && *str)
	{
		i *= 10;
		i += (*str - '0');
		str++;
	}
	return (i * sign);
}
